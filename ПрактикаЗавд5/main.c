#include <stdio.h>

// Функція для обчислення кількості шуканих послідовностей
int countSequences(int n) {
    int dp[n+1];
    dp[0] = 1;
    dp[1] = 2;
    dp[2] = 4;

    // Заповнення масиву dp
    for (int i = 3; i <= n; i++) {
        dp[i] = (dp[i-1] + dp[i-2] + dp[i-3]) % 12345;
    }

    return dp[n];
}

int main() {
    int n;

    // Введення довжини послідовності від користувача
    printf("Введіть довжину послідовності (1 < n < 10000): ");
    scanf("%d", &n);

    // Обчислення кількості шуканих послідовностей
    int result = countSequences(n);

    // Виведення результату
    printf("Кількість шуканих послідовностей: %d\n", result);

    return 0;
}


