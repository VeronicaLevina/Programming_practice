#include <stdio.h>
#include <math.h>

int main() {
    double x1, y1, r1, x2, y2, r2;

    // Введення координат і радіусів перших двох колів
    printf("Введіть координати та радіус першого кола (x1 y1 r1): ");
    scanf("%lf %lf %lf", &x1, &y1, &r1);
    printf("Введіть координати та радіус другого кола (x2 y2 r2): ");
    scanf("%lf %lf %lf", &x2, &y2, &r2);

    // Обчислення відстані між центрами колів
    double distance = sqrt(pow(x2 - x1, 2) + pow(y2 - y1, 2));

    // Визначення кількості точок перетину
    if (distance > r1 + r2) {
        printf("Кола не перетинаються.\n");
        printf("Кількість точок перетину: 0\n");
    } else if (distance == r1 + r2) {
        printf("Кола торкаються зовнішньо.\n");
        printf("Кількість точок перетину: 1\n");
    } else if (distance < fabs(r1 - r2)) {
        printf("Одне коло знаходиться всередині іншого.\n");
        printf("Кількість точок перетину: 0\n");
    } else if (distance == fabs(r1 - r2)) {
        printf("Одне коло торкається всередині іншого.\n");
        printf("Кількість точок перетину: 1\n");
    } else {
        printf("Кола перетинаються в двох точках.\n");
        printf("Кількість точок перетину: 2\n");
    }

    return 0;
}


